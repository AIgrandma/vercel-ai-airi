export async function POST(req: NextRequest) {
  try {
    const rawBody = await req.text();
    const signature = req.headers.get("x-line-signature") || "";

    if (!verifySignature(rawBody, signature)) {
      console.error("署名検証失敗");
      return NextResponse.json({ ok: false }, { status: 401 });
    }

    const body = JSON.parse(rawBody);
    const events = body.events || [];

    for (const event of events) {
      const userId = event.source?.userId;
      const replyToken = event.replyToken;
      if (!userId) continue;

      // 友達追加
      if (event.type === "follow") {
        await ensureUser(userId);
        const { count } = await getSupabase()
          .from("users_profile")
          .select("id", { count: "exact", head: true });
        const isFounder = (count ?? 0) <= 100;
        if (isFounder) {
          await getSupabase()
            .from("users_profile")
            .update({ is_founder: true })
            .eq("id", userId);
        }
        await sendWelcomeMessage(replyToken, isFounder, userId);
        continue;
      }

      // テキストメッセージ
      if (event.type === "message" && event.message?.type === "text") {
        const userMsg = event.message.text.trim();
        const msgClass = classifyMessage(userMsg);

        // 分析ログ(fire-and-forget・応答をブロックしない)
        const topicCategory = categorizeTopicQuick(userMsg);
        logMessageAnalytics(userId, userMsg, msgClass, topicCategory);

        try {
          await ensureUser(userId);

          // v3.4: エリア回答待ちチェック
          const areaProfile = await getProfile(userId);
          if (
            areaProfile?.area_asked_at &&
            !areaProfile?.area_answered_at &&
            !areaProfile?.area_declined
          ) {
            const detected = detectArea(userMsg);

            if (detected.declined) {
              await getSupabase()
                .from("users_profile")
                .update({
                  area_declined: true,
                  area_answered_at: new Date().toISOString(),
                })
                .eq("id", userId);
              const reply =
                "わかった🤍 また気が向いたら教えてね ദ്ദი^ᴗ ̫ ᴗ^₎\nじゃあ、何でも気軽に話してね";
              await Promise.all([
                saveMessages(userId, userMsg, reply),
                replyToLine(replyToken, reply),
              ]);
              continue;
            }

            if (detected.region || detected.country) {
              await getSupabase()
                .from("users_profile")
                .update({
                  area_region: detected.region || null,
                  area_country: detected.country || null,
                  area_answered_at: new Date().toISOString(),
                })
                .eq("id", userId);
              const reply = detected.region
                ? `${detected.region}なんだね🎀 ありがとう✦.*\n近くで使える情報が整ったら案内するね ദ്ദი＞ᴗ＜)🎀✧\n\nじゃあ、何でも気軽に話してね`
                : `${detected.country}に住んでるんだ🪽 教えてくれてありがとう ദ്ദი^ᴗ ̫ ᴗ^₎\n海外からも来てくれて嬉しい⟡.·*.\n\nじゃあ、何でも気軽に話してね`;
              await Promise.all([
                saveMessages(userId, userMsg, reply),
                replyToLine(replyToken, reply),
              ]);
              continue;
            }
            // エリア認識できなかった → 通常フローに流す
          }

          // テンプレ即レス（improvement_request / inappropriate / reaction / greeting / emotion）
          const templateReply = getTemplateReply(msgClass, userMsg);
          if (templateReply) {
            const tasks: Promise<any>[] = [
              saveMessages(userId, userMsg, templateReply),
              replyToLine(replyToken, templateReply),
            ];
            if (msgClass === "improvement_request") {
              const feedbackText = extractFeedbackText(userMsg);
              tasks.push(saveImprovementFeedback(userId, feedbackText, userMsg));
            }
            await Promise.all(tasks);
            continue;
          }

          // LLM 処理（short / long）
          showLoadingAnimation(userId); // fire-and-forget: await しない
          const [profile, history] = await Promise.all([
            getProfile(userId),
            getHistory(userId),
          ]);
          const callName = profile?.preferred_call_name ?? "";
          const memoryLine = callName
            ? `\nユーザーの呼び名は「${callName}」です。自然な頻度で名前を呼んでください。`
            : "";

          const messages: { role: string; content: string }[] = [
            { role: "system", content: SYSTEM_PROMPT + memoryLine },
            ...history.map((m: { role: string; content: string }) => ({
              role: m.role,
              content: m.content,
            })),
            { role: "user", content: userMsg },
          ];

          const isLong = msgClass === "long";
          const model = isLong ? "gpt-4o" : "gpt-4o-mini";
          const maxTokens = isLong ? 1500 : 600;

          const completion = await getOpenAI().chat.completions.create({
            model,
            messages: messages as any,
            max_tokens: maxTokens,
            temperature: 0.85,
          });

          let aiReply =
            completion.choices[0]?.message?.content?.trim() ||
            "ちょっと考え中…もう一度送ってみてもらえる?🌸";

          // Truncation 検知 + 自動継続(最大1回)
          const finishReason = completion.choices[0]?.finish_reason;
          if (finishReason === "length" && aiReply && aiReply.length > 0) {
            console.warn(`[v2.9] Response truncated by max_tokens, requesting continuation`);
            try {
              const continuationMessages = [
                ...messages,
                { role: "assistant", content: aiReply },
                {
                  role: "user",
                  content: "続きをお願い。前の応答の続きから自然につなげて、最後まで完結させて。新しい挨拶や前置きは入れず、文の途中から続けてください。",
                },
              ];

              const continuation = await getOpenAI().chat.completions.create({
                model,
                messages: continuationMessages as any,
                max_tokens: maxTokens,
                temperature: 0.85,
              });

              const contText = continuation.choices[0]?.message?.content?.trim() || "";
              if (contText) {
                const cleanedCont = contText.replace(/^(続きです|続きです。|続き:|では続きです。?|それでは続きを。?)\s*/i, "");
                aiReply = aiReply + cleanedCont;
              }
            } catch (err) {
              console.error("[v2.9] Continuation failed:", err);
            }
          }

          // v3.3: 顔文字強制(LLMが付けなかった場合のセーフティネット)
          const emotionTrigger = detectEmotionTrigger(userMsg);
          aiReply = enforceKaomoji(aiReply, emotionTrigger);

          // 改善要望募集メッセージの自然挿入チェック
          const appendix = await maybeAppendImprovementPrompt(userId, profile);
          if (appendix) {
            aiReply = `${aiReply}\n\n${appendix}`;
          }

          await Promise.all([
            saveMessages(userId, userMsg, aiReply),
            replyToLine(replyToken, aiReply),
          ]);
        } catch (err) {
          console.error("メッセージ処理エラー:", err);
          await replyToLine(
            replyToken,
            "ごめんね、ちょっと調子悪いみたい🙏\nもう一度送ってみてもらえる?"
          ).catch(() => {});
        }
      }
    }

    return NextResponse.json({ ok: true });
  } catch (err) {
    console.error("Webhookエラー:", err);
    return NextResponse.json({ ok: true });
  }
}

