async function getHistory(userId: string) {
  const { data } = await getSupabase()
    .from("conversation_messages")
    .select("role, content")
    .eq("user_id", userId)
    .order("created_at", { ascending: false })
    .limit(12);
  return (data ?? []).reverse();
}

